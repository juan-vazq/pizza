     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->ajax())
            return Pedido::selectRaw('pedidos.*, pizzas.nombre, users.name')
            ->join('pizzas', 'pedidos.pizzas', '=', 'pizzas.id')
            ->join('users', 'pedidos.user_id', '=', 'users.id')->get();
    }

